package com.arslaner.human;

public class Insan {

    String ad;
    String sad;
    int yas;

    public Insan(String ad, String sad, int yas) {
        this.ad = ad;
        this.sad = sad;
        this.yas = yas;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getSad() {
        return sad;
    }

    public void setSad(String sad) {
        this.sad = sad;
    }

    public int getYas() {
        return yas;
    }

    public void setYas(int yas) {
        this.yas = yas;
    }
}
